#include "../includes/fractol.h"

int	ft_mouse(int mousecode, t_vars *ptr)
{
	if (mousecode == 1)
		ptr->pos.zoom += 0.05;
	if (mousecode == 2)
		ptr->pos.zoom -= 0.05;
	return (0);
}
