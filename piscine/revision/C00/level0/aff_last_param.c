#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
	ft_putchar('\n');
}

int	main(int argc, char **argv)
{
	int	a;

	a = argc - 1;
	if (a >= 1 && argv[a])
	{
		ft_putstr(argv[a]);
	}
	else
		ft_putchar('\n');
	return (0);
}
