#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int	tmp;

	tmp = *a;
	*a = *b;
	*b = tmp;
}

int	main(void)
{
	int	a = 5;
	int	b = 7;

	ft_swap(&a, &b);
	printf("%d %d\n", a, b);
	return (0);
}
